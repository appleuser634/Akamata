//! Type-safe client generation from app route metadata.
//!
//! Walks the same `app.routes[i].meta` chain that `openapi.zig` uses and
//! emits a runnable client module. Currently supports two targets:
//!
//!   * **TypeScript** (`.typescript`) — one async function per documented
//!     endpoint, fetch-based, with TS interfaces for every request/response
//!     type.
//!   * **Zig** (`.zig`) — uses `am.http_client.send` under the hood. Useful
//!     for service-to-service calls in the same monorepo.
//!
//! Routes registered via the bare `app.get/post/...` helpers (no meta) are
//! skipped — same rule as the OpenAPI generator.

const std = @import("std");
const openapi = @import("openapi.zig");

pub const Target = enum {
    typescript,
    zig,
};

pub const Options = struct {
    target: Target,
    /// Base URL used in the generated client. Can be overridden by a
    /// runtime argument at the call site.
    base_url: []const u8 = "",
};

pub fn generate(comptime AppT: type, app: *AppT, gpa: std.mem.Allocator, opts: Options) ![]u8 {
    var arena_state: std.heap.ArenaAllocator = .init(gpa);
    defer arena_state.deinit();
    const arena = arena_state.allocator();

    // Reuse the OpenAPI schema collector so types are emitted exactly once.
    var builder: openapi.SpecBuilder = .init(arena);

    var ops: std.ArrayList(OpEntry) = .empty;
    const route_views = try app.routeViews(arena);
    for (route_views) |r| {
        if (r.kind != .http) continue;
        const meta = r.meta orelse continue;
        const refs = try meta.schema_fn(arena, &builder);
        try ops.append(arena, .{
            .method = r.method,
            .raw_path = r.path,
            .meta = meta,
            .refs = refs,
        });
    }

    var aw: std.Io.Writer.Allocating = try .initCapacity(arena, 4096);
    const w = &aw.writer;

    switch (opts.target) {
        .typescript => try emitTypescript(w, opts.base_url, ops.items, &builder),
        .zig => try emitZig(w, opts.base_url, ops.items, &builder),
    }
    return gpa.dupe(u8, aw.written());
}

const OpEntry = struct {
    method: @import("http/request.zig").Method,
    raw_path: []const u8,
    meta: *const openapi.EndpointMeta,
    refs: openapi.EndpointMeta.Refs,
};

// =========================================================================
// TypeScript emitter
// =========================================================================

fn emitTypescript(
    w: *std.Io.Writer,
    base_url: []const u8,
    ops: []const OpEntry,
    builder: *openapi.SpecBuilder,
) !void {
    try w.writeAll(
        \\// Generated by Akamata client_gen. Do not edit by hand.
        \\
        \\
    );
    try w.print("const DEFAULT_BASE = {f};\n\n", .{jsonString(base_url)});

    // Type definitions for every collected schema.
    var sch_it = builder.schemas.iterator();
    while (sch_it.next()) |entry| {
        const name = entry.key_ptr.*;
        const json_schema = entry.value_ptr.*;
        try w.print("export interface {s} ", .{name});
        try writeTsInterfaceFromJsonSchema(w, json_schema);
        try w.writeAll("\n\n");
    }

    // Client factory.
    try w.writeAll(
        \\export interface ClientOptions {
        \\  baseUrl?: string;
        \\  headers?: Record<string, string>;
        \\  fetch?: typeof fetch;
        \\}
        \\
        \\export function createClient(opts: ClientOptions = {}) {
        \\  const base = opts.baseUrl ?? DEFAULT_BASE;
        \\  const f = opts.fetch ?? globalThis.fetch;
        \\  const baseHeaders = opts.headers ?? {};
        \\  return {
        \\
    );

    for (ops) |op| {
        try emitTypescriptOperation(w, op);
    }

    try w.writeAll(
        \\  };
        \\}
        \\
    );
}

fn emitTypescriptOperation(w: *std.Io.Writer, op: OpEntry) !void {
    try w.writeAll("    ");
    try writeOperationFnName(w, op.method, op.raw_path);
    try w.writeAll(": async (");

    // Path params first.
    var has_path_params = false;
    {
        var seg_it = std.mem.splitScalar(u8, op.raw_path, '/');
        while (seg_it.next()) |seg| {
            if (seg.len > 0 and seg[0] == ':') {
                if (has_path_params) try w.writeAll(", ");
                try w.print("{s}: string", .{seg[1..]});
                has_path_params = true;
            }
        }
    }
    // Body if any.
    if (op.refs.request) |req_ref| {
        if (has_path_params) try w.writeAll(", ");
        try w.print("body: {s}", .{refToTypeName(req_ref)});
    }
    // Query params: an inline anonymous interface so callers see the
    // available keys via tooltips. Always optional at the TS level —
    // the OpenAPI spec carries the required-ness.
    const has_query = op.meta.query_ts_fields_fn != null;
    if (has_query) {
        if (has_path_params or op.refs.request != null) try w.writeAll(", ");
        try w.writeAll("query: {\n");
        try op.meta.query_ts_fields_fn.?(w);
        try w.writeAll("    } = {}");
    }
    try w.writeAll("): Promise<");
    if (op.refs.response) |res_ref| {
        try w.writeAll(refToTypeName(res_ref));
    } else {
        try w.writeAll("void");
    }
    try w.writeAll("> => {\n");

    // Build the path string with template literals for params.
    try w.writeAll("      const path = `");
    {
        var i: usize = 0;
        while (i < op.raw_path.len) {
            if (op.raw_path[i] == ':') {
                const start = i + 1;
                var end = start;
                while (end < op.raw_path.len and op.raw_path[end] != '/') : (end += 1) {}
                try w.print("${{{s}}}", .{op.raw_path[start..end]});
                i = end;
            } else {
                try w.writeByte(op.raw_path[i]);
                i += 1;
            }
        }
    }
    try w.writeAll("`;\n");

    if (has_query) {
        // Build the querystring with URLSearchParams, skipping undefined values.
        try w.writeAll(
            \\      const _qs = new URLSearchParams();
            \\      for (const [k, v] of Object.entries(query)) {
            \\        if (v !== undefined && v !== null) _qs.set(k, String(v));
            \\      }
            \\      const _qstr = _qs.toString();
            \\      const _full = `${base}${path}${_qstr ? `?${_qstr}` : ""}`;
            \\
        );
    } else {
        try w.writeAll("      const _full = `${base}${path}`;\n");
    }

    try w.print(
        \\      const res = await f(_full, {{
        \\        method: "{s}",
        \\        headers: {{ "content-type": "application/json", ...baseHeaders }},
        \\
    , .{lowerMethod(op.method)});
    if (op.refs.request != null) {
        try w.writeAll("        body: JSON.stringify(body),\n");
    }
    try w.writeAll("      });\n");
    try w.writeAll("      if (!res.ok) throw new Error(`HTTP ${res.status}`);\n");
    if (op.refs.response != null) {
        try w.writeAll("      return res.json();\n");
    } else {
        try w.writeAll("      return;\n");
    }
    try w.writeAll("    },\n");
}

fn writeOperationFnName(w: *std.Io.Writer, method: @import("http/request.zig").Method, path: []const u8) !void {
    // Build a JS identifier from method + path:
    //   GET /users/:id           -> getUsersById
    //   POST /users              -> postUsers
    try w.writeAll(lowerMethod(method));
    var capitalize = true;
    for (path) |c| {
        if (c == '/' or c == '-' or c == '_') {
            capitalize = true;
            continue;
        }
        if (c == ':') {
            // Convention: ":id" -> "ById"
            try w.writeAll("By");
            capitalize = true;
            continue;
        }
        if (capitalize) {
            try w.writeByte(std.ascii.toUpper(c));
            capitalize = false;
        } else {
            try w.writeByte(c);
        }
    }
}

fn operationFnName(_: []u8, method: @import("http/request.zig").Method, path: []const u8) ![]const u8 {
    _ = method;
    return path;
}

fn refToTypeName(ref: []const u8) []const u8 {
    // "#/components/schemas/User" -> "User"
    var last_slash: usize = 0;
    for (ref, 0..) |c, i| {
        if (c == '/') last_slash = i + 1;
    }
    return ref[last_slash..];
}

fn lowerMethod(m: @import("http/request.zig").Method) []const u8 {
    return switch (m) {
        .GET => "get",
        .POST => "post",
        .PUT => "put",
        .DELETE => "delete",
        .PATCH => "patch",
        .OPTIONS => "options",
        .HEAD => "head",
    };
}

// Minimal JSON-schema-to-TS-interface converter. The schemas we produce
// have a known shape (writeTypeSchema in openapi.zig); we walk that shape
// directly rather than parsing JSON.
fn writeTsInterfaceFromJsonSchema(w: *std.Io.Writer, schema: []const u8) std.Io.Writer.Error!void {
    // The OpenAPI schemas we emit are top-level `{"type":"object","properties":{...},"required":[...]}`.
    // Reparse that to TS.
    try w.writeAll("{\n");
    // properties section
    if (findField(schema, "\"properties\":")) |props_start| {
        const props_obj = parseObject(schema[props_start..]) orelse {
            try w.writeAll("}");
            return;
        };
        // required list
        var required: []const u8 = "";
        if (findField(schema, "\"required\":")) |req_start| {
            if (parseArray(schema[req_start..])) |arr| required = arr;
        }
        var it = objectFieldsIterator(props_obj);
        while (it.next()) |field| {
            const optional = !arrayContainsString(required, field.name);
            try w.print("  {s}{s}: ", .{ field.name, if (optional) "?" else "" });
            try writeTsTypeFromJsonSchema(w, field.value);
            try w.writeAll(";\n");
        }
    }
    try w.writeAll("}");
}

fn writeTsTypeFromJsonSchema(w: *std.Io.Writer, schema: []const u8) std.Io.Writer.Error!void {
    // Find "type":
    if (findField(schema, "\"type\":")) |t_start| {
        const type_str = parseString(schema[t_start..]) orelse "any";
        if (std.mem.eql(u8, type_str, "string")) {
            try w.writeAll("string");
            return;
        }
        if (std.mem.eql(u8, type_str, "integer") or std.mem.eql(u8, type_str, "number")) {
            try w.writeAll("number");
            return;
        }
        if (std.mem.eql(u8, type_str, "boolean")) {
            try w.writeAll("boolean");
            return;
        }
        if (std.mem.eql(u8, type_str, "null")) {
            try w.writeAll("null");
            return;
        }
        if (std.mem.eql(u8, type_str, "array")) {
            if (findField(schema, "\"items\":")) |i_start| {
                try w.writeAll("Array<");
                try writeTsTypeFromJsonSchema(w, schema[i_start..]);
                try w.writeAll(">");
                return;
            }
            try w.writeAll("Array<any>");
            return;
        }
        if (std.mem.eql(u8, type_str, "object")) {
            try writeTsInterfaceFromJsonSchema(w, schema);
            return;
        }
    }
    // oneOf: nullable union
    if (findField(schema, "\"oneOf\":")) |_| {
        // Pretty-printing the union is overkill; emit a permissive type.
        try w.writeAll("any");
        return;
    }
    try w.writeAll("any");
}

// =========================================================================
// Zig emitter
// =========================================================================

fn emitZig(
    w: *std.Io.Writer,
    base_url: []const u8,
    ops: []const OpEntry,
    builder: *openapi.SpecBuilder,
) !void {
    try w.writeAll(
        \\// Generated by Akamata client_gen. Do not edit by hand.
        \\const std = @import("std");
        \\const am = @import("akamata");
        \\
        \\pub const default_base_url =
    );
    try w.print("{f};\n\n", .{jsonString(base_url)});

    // Type stubs: emit Zig struct definitions matching the schemas.
    var sch_it = builder.schemas.iterator();
    while (sch_it.next()) |entry| {
        const name = entry.key_ptr.*;
        const json_schema = entry.value_ptr.*;
        try w.print("pub const {s} = struct ", .{name});
        try writeZigStructFromJsonSchema(w, json_schema);
        try w.writeAll(";\n\n");
    }

    try w.writeAll(
        \\pub const Client = struct {
        \\    gpa: std.mem.Allocator,
        \\    base_url: []const u8,
        \\
        \\    pub fn init(gpa: std.mem.Allocator, base_url: []const u8) Client {
        \\        return .{ .gpa = gpa, .base_url = base_url };
        \\    }
        \\
    );

    for (ops) |op| {
        try emitZigOperation(w, op);
    }

    try w.writeAll(
        \\};
        \\
    );
}

fn emitZigOperation(w: *std.Io.Writer, op: OpEntry) !void {
    try w.writeAll("    pub fn ");
    try writeOperationFnName(w, op.method, op.raw_path);
    try w.writeAll("(self: *Client");

    var seg_it = std.mem.splitScalar(u8, op.raw_path, '/');
    while (seg_it.next()) |seg| {
        if (seg.len > 0 and seg[0] == ':') {
            try w.print(", {s}: []const u8", .{seg[1..]});
        }
    }
    if (op.refs.request) |req_ref| {
        try w.print(", body: {s}", .{refToTypeName(req_ref)});
    }
    try w.writeAll(") !");
    if (op.refs.response) |res_ref| {
        try w.writeAll(refToTypeName(res_ref));
    } else {
        try w.writeAll("void");
    }
    try w.writeAll(" {\n");
    // For brevity the Zig client is a stub — apps that want it can flesh
    // out the actual HTTP send via am.http_client. The point is to lock in
    // type safety and route names.
    try w.writeAll("        _ = self;\n");
    // Suppress unused-arg warnings.
    var sit = std.mem.splitScalar(u8, op.raw_path, '/');
    while (sit.next()) |seg| {
        if (seg.len > 0 and seg[0] == ':') {
            try w.print("        _ = {s};\n", .{seg[1..]});
        }
    }
    if (op.refs.request != null) try w.writeAll("        _ = body;\n");
    try w.writeAll("        @panic(\"client_gen Zig target is a type-stub; wire am.http_client.send manually\");\n");
    try w.writeAll("    }\n\n");
}

fn writeZigStructFromJsonSchema(w: *std.Io.Writer, schema: []const u8) std.Io.Writer.Error!void {
    try w.writeAll("{\n");
    if (findField(schema, "\"properties\":")) |props_start| {
        const props_obj = parseObject(schema[props_start..]) orelse {
            try w.writeAll("}");
            return;
        };
        var required: []const u8 = "";
        if (findField(schema, "\"required\":")) |req_start| {
            if (parseArray(schema[req_start..])) |arr| required = arr;
        }
        var it = objectFieldsIterator(props_obj);
        while (it.next()) |field| {
            const optional = !arrayContainsString(required, field.name);
            try w.print("    {s}: ", .{field.name});
            if (optional) try w.writeAll("?");
            try writeZigTypeFromJsonSchema(w, field.value);
            try w.writeAll(",\n");
        }
    }
    try w.writeAll("}");
}

fn writeZigTypeFromJsonSchema(w: *std.Io.Writer, schema: []const u8) std.Io.Writer.Error!void {
    if (findField(schema, "\"type\":")) |t_start| {
        const type_str = parseString(schema[t_start..]) orelse "[]const u8";
        if (std.mem.eql(u8, type_str, "string")) {
            try w.writeAll("[]const u8");
            return;
        }
        if (std.mem.eql(u8, type_str, "integer")) {
            try w.writeAll("i64");
            return;
        }
        if (std.mem.eql(u8, type_str, "number")) {
            try w.writeAll("f64");
            return;
        }
        if (std.mem.eql(u8, type_str, "boolean")) {
            try w.writeAll("bool");
            return;
        }
        if (std.mem.eql(u8, type_str, "array")) {
            if (findField(schema, "\"items\":")) |i_start| {
                try w.writeAll("[]const ");
                try writeZigTypeFromJsonSchema(w, schema[i_start..]);
                return;
            }
        }
    }
    try w.writeAll("[]const u8");
}

// =========================================================================
// Mini JSON walker (we only need to inspect schemas we just wrote ourselves,
// so we can rely on the exact shape — no need for std.json.parseFromSlice).
// =========================================================================

/// Find the byte offset just after `key` (a quoted-string-with-colon, e.g.
/// `"properties":`). Returns null if not found at depth 0 of the containing
/// object.
fn findField(blob: []const u8, key: []const u8) ?usize {
    // Naive substring search at the top level. Since our schemas are
    // emitted by writeTypeSchema and are well-formed, this works without
    // a real JSON parser.
    var depth: i32 = 0;
    var i: usize = 0;
    while (i < blob.len) {
        const c = blob[i];
        if (c == '{') depth += 1;
        if (c == '}') depth -= 1;
        if (c == '"' and depth >= 0) {
            // Try matching key
            if (i + key.len <= blob.len and std.mem.eql(u8, blob[i .. i + key.len], key)) {
                if (depth == 1 or (depth == 0 and i == 0)) {
                    return i + key.len;
                }
            }
            // Skip string
            i += 1;
            while (i < blob.len and blob[i] != '"') {
                if (blob[i] == '\\') i += 1;
                i += 1;
            }
        }
        i += 1;
    }
    return null;
}

fn parseString(blob: []const u8) ?[]const u8 {
    var i: usize = 0;
    while (i < blob.len and blob[i] != '"') : (i += 1) {}
    if (i == blob.len) return null;
    const start = i + 1;
    i += 1;
    while (i < blob.len and blob[i] != '"') : (i += 1) {
        if (blob[i] == '\\') i += 1;
    }
    if (i == blob.len) return null;
    return blob[start..i];
}

fn parseObject(blob: []const u8) ?[]const u8 {
    var i: usize = 0;
    while (i < blob.len and blob[i] != '{') : (i += 1) {}
    if (i == blob.len) return null;
    const start = i;
    var depth: i32 = 0;
    while (i < blob.len) {
        if (blob[i] == '{') depth += 1;
        if (blob[i] == '}') {
            depth -= 1;
            if (depth == 0) return blob[start .. i + 1];
        }
        if (blob[i] == '"') {
            i += 1;
            while (i < blob.len and blob[i] != '"') {
                if (blob[i] == '\\') i += 1;
                i += 1;
            }
        }
        i += 1;
    }
    return null;
}

fn parseArray(blob: []const u8) ?[]const u8 {
    var i: usize = 0;
    while (i < blob.len and blob[i] != '[') : (i += 1) {}
    if (i == blob.len) return null;
    const start = i;
    var depth: i32 = 0;
    while (i < blob.len) {
        if (blob[i] == '[') depth += 1;
        if (blob[i] == ']') {
            depth -= 1;
            if (depth == 0) return blob[start .. i + 1];
        }
        i += 1;
    }
    return null;
}

fn arrayContainsString(arr: []const u8, needle: []const u8) bool {
    var i: usize = 0;
    while (i < arr.len) {
        if (arr[i] == '"') {
            const s = parseString(arr[i..]) orelse return false;
            if (std.mem.eql(u8, s, needle)) return true;
            i += s.len + 2;
            continue;
        }
        i += 1;
    }
    return false;
}

/// Walks `{ "foo": <value>, "bar": <value>, ... }`. Returns each (name,
/// value-slice) pair.
const ObjFieldIt = struct {
    blob: []const u8,
    pos: usize,

    const Pair = struct { name: []const u8, value: []const u8 };

    pub fn next(self: *ObjFieldIt) ?Pair {
        // Skip to next '"' that opens a key.
        while (self.pos < self.blob.len) {
            const c = self.blob[self.pos];
            if (c == '"') break;
            if (c == '}') return null;
            self.pos += 1;
        } else return null;
        const key = parseString(self.blob[self.pos..]) orelse return null;
        self.pos += key.len + 2; // skip "key"
        // skip ':' + whitespace
        while (self.pos < self.blob.len and self.blob[self.pos] != ':') : (self.pos += 1) {}
        self.pos += 1;
        while (self.pos < self.blob.len and (self.blob[self.pos] == ' ' or self.blob[self.pos] == '\t')) : (self.pos += 1) {}
        // value is either a string, number, object, array, true/false/null.
        const val_start = self.pos;
        const c = self.blob[self.pos];
        if (c == '{') {
            const v = parseObject(self.blob[self.pos..]) orelse return null;
            self.pos += v.len;
            return .{ .name = key, .value = v };
        }
        if (c == '[') {
            const v = parseArray(self.blob[self.pos..]) orelse return null;
            self.pos += v.len;
            return .{ .name = key, .value = v };
        }
        if (c == '"') {
            const v = parseString(self.blob[self.pos..]) orelse return null;
            self.pos += v.len + 2;
            return .{ .name = key, .value = self.blob[val_start .. val_start + v.len + 2] };
        }
        // bare token (number / bool / null)
        while (self.pos < self.blob.len and self.blob[self.pos] != ',' and self.blob[self.pos] != '}') : (self.pos += 1) {}
        return .{ .name = key, .value = self.blob[val_start..self.pos] };
    }
};

fn objectFieldsIterator(obj: []const u8) ObjFieldIt {
    // obj starts with '{'.
    return .{ .blob = obj, .pos = 1 };
}

/// Format adapter that writes a JSON string literal.
fn jsonString(s: []const u8) JsonStringFormatter {
    return .{ .s = s };
}

const JsonStringFormatter = struct {
    s: []const u8,
    pub fn format(self: JsonStringFormatter, w: *std.Io.Writer) std.Io.Writer.Error!void {
        try w.writeByte('"');
        for (self.s) |c| switch (c) {
            '"' => try w.writeAll("\\\""),
            '\\' => try w.writeAll("\\\\"),
            '\n' => try w.writeAll("\\n"),
            else => try w.writeByte(c),
        };
        try w.writeByte('"');
    }
};
